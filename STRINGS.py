# Display a single string
print("Hello, World!")

# Display multiple strings
print("This is a sample Python code.")
print("You can display multiple lines by using multiple print statements.")

# Display variables with strings
name = "Alice"
age = 30
print("My name is", name, "and I am", age, "years old.")

# Display formatted strings using f-strings (Python 3.6+)
greeting = "Hello"
recipient = "John"
print(f"{greeting}, {recipient}!")

# Display strings with escape characters
print("This is a line with a new line character:\nLine 2 starts here.")
print("You can also use tab characters:\tTabbed Text.")

# Concatenate strings
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print("Full name:", full_name)

# Display strings inside triple quotes (for multi-line strings)
multiline_string = """
This is a multi-line string.
It can span multiple lines.
"""
print(multiline_string)
